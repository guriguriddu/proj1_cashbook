// Gemini 거래내역 추출 프롬프트 (사진 OCR / 영상 인식 공용)
// 사진은 한 장의 캡쳐, 영상은 스크롤하며 녹화한 화면 — 프롬프트 본문은 동일하게 사용하고
// 도입부 한 줄만 매체에 맞춰 바꾼다.
export function buildExtractionPrompt(opts: { isVideo?: boolean } = {}): string {
  const year = new Date().getFullYear()

  const intro = opts.isVideo
    ? `이것은 은행 계좌 거래 내역, 카드 결제 내역, 또는 간편결제 앱 소비 내역을 위에서 아래로 스크롤하며 녹화한 화면 영상입니다.
영상 전체(처음부터 끝까지 모든 구간)에 걸쳐 등장하는 모든 "출금/지출" 항목을 빠짐없이 추출해주세요.
★ 스크롤 중 같은 거래가 여러 프레임에 반복해서 보이면 반드시 한 번만 추출하세요 (중복 출력 금지).`
    : `이 이미지는 은행 계좌 거래 내역, 카드 결제 내역, 또는 간편결제 앱 소비 내역 캡쳐입니다.
이미지에 보이는 모든 "출금/지출" 항목을 빠짐없이 추출해주세요.`

  return `${intro}

★ 절대 제외하지 말 것: PAYCO결제, 카카오페이, 네이버페이, 토스, 삼성페이, 애플페이로 나가는 금액.
  "내 하나계좌 → PAYCO결제 -70,000원" 같은 항목은 반드시 포함.
  ★ 중요: 은행계좌 → 간편결제 충전 항목은 사용처에 화살표(→)를 반드시 보존해서 기재.
    예) "내 하나계좌 → 카카오페이 -287,412원" → 사용처: "하나→카카오페이"
    예) "내 KB국민 → 네이버페이 머니 -50,000원" → 사용처: "KB→네이버페이"
    예) "내 하나계좌 → PAYCO결제 -70,000원" → 사용처: "하나→PAYCO결제"

포함 대상: 카드결제, ATM출금, 간편결제 이체, 구독/정기결제, 모든 출금(-) 항목, 개인에게 보내는 이체(예: 입주청소, 과외비 등)
제외 대상: 내 계좌끼리 단순 이동(예: 하나→KB국민 내 계좌), 입금(+)/월급/이자/환급, 주식/적금 이체
취소(strikethrough) 표시된 항목은 제외

★ 날짜 구분값 해석 규칙 (토스·뱅크샐러드·카카오뱅크 등 앱 공통):
  날짜 구분값 = 거래 목록 안에 "5월 10일 일요일"처럼 날짜만 단독으로 적힌 텍스트 줄.
  ※ 달력 위젯(캘린더)의 숫자(10, 11, 12...)는 날짜 구분값이 아닙니다.
  각 거래의 날짜 = 해당 항목 바로 위에 있는(가장 가까운 위쪽) 날짜 구분값의 날짜.
  어떤 항목 위에 날짜 구분값이 보이지 않는 경우: 상단 달력에서 진하게 표시(하이라이트/선택)된 날짜를 그대로 사용하세요.
  ※ 절대로 날짜에 +1일을 더하지 마세요. 달력 하이라이트 날짜를 정확히 그대로 사용.

  ★★ 날짜 경계 규칙 — 가장 흔한 오류이니 반드시 지킬 것:
    거래는 항상 자기 "위쪽"의 가장 가까운 날짜 구분값에 속합니다.
    ① 거래 "아래"에 다음(더 이전) 날짜 구분값이 보여도, 그 아래 날짜를 절대 쓰지 마세요.
    ② 영상에서 스크롤로 날짜 구분값이 화면 위로 사라졌어도, 새 구분값이 나오기 전까지는 마지막으로 본 날짜를 계속 적용하세요(밑으로 새 구분값이 나타나는 순간부터 그 날짜로 바뀜).
    예) 화면에 "5월 5일" 구분값 → 챗GPT·쿠팡 거래 → "5월 4일" 구분값 순서로 나오면,
        챗GPT·쿠팡은 5월 5일입니다 (절대 5월 4일이 아님). "5월 4일"은 그 아래에 오는 거래부터 적용.

★ 금액 출력 규칙:
  실제 지출 항목은 항상 양수로 출력. 앱 UI에서 음수로 표시돼도 지출이면 양수로.
  단, 카드 명세서에서 빨간색이거나 "환불"·"취소"가 명시된 항목은 음수(-)로 출력 → 시스템이 자동 제외 처리.
  예) 삼성카드 환불 -72,969원(빨간) → 금액: -72969
  예) 토스은행 출금 -30,000원(일반 지출) → 금액: 30000
  한 항목에 취소선 금액과 최종 금액이 함께 있으면 → 취소선 없는 최종 금액만 사용.
  예) "-4,000  -735원"에서 4,000에 취소선 있으면 → 금액: 735

★ 포함/제외 기준:
  포함: 카카오페이·네이버페이·토스페이·무신사머니·쇼핑몰페이 등 소비 목적 이동
    예) "하나 → 네이버페이 머니 -30,000원" → 포함 (쇼핑에 쓸 돈)
    예) "내 KB국민계좌 → 무신사 머니 -20,000원" → 포함 (쇼핑에 쓸 돈)
    예) "내 하나계좌 → PAYCO결제 -70,000원" → 포함
  제외: 내 계좌끼리 단순 이동, 실제 주식·펀드 투자
    예) "하나 → 내 KB국민" → 제외 (내 계좌 이동)
    예) "카카오페이증권 → 삼성전자 매수" → 제외 (실제 주식 투자)
  ★ "충전 후 송금" 구분 (자주 틀리는 부분):
    (a) 토스에 "충전 후 송금"이 명시 + 받는 쪽이 사람(타인) → 실지출. 사용처에 "충전 후 송금" 유지.
       예) "카카오페이 충전 후 송금 | 카카오뱅크 → 박*민" → 사용처: "카카오페이 충전 후 송금 → 박*민"
    (b) 단순 지갑 충전(받는 쪽이 카카오페이/카카오페이증권/네이버페이 등 = 사람이 아님) → "충전 후 송금"이 절대 아님!
       사용처에 목적지를 그대로 보존하고 "충전 후 송금"이라고 쓰지 말 것.
       예) "카카오페이 충전 | 카카오뱅크 → 카카오페이증권" → 사용처: "카카오뱅크 → 카카오페이증권"
       예) "내 KB국민계좌 → 카카오페이" → 사용처: "KB → 카카오페이"
  제외: 입금(+)/월급/이자/환급, 주식·적금 이체

날짜에 년도가 없으면 ${year}년으로 가정.

카테고리 (반드시 아래 ID 중 하나만 사용):
food, cafe, shopping, transport, fixed, telecom, insurance, education, travel, cash, other

간편결제(PAYCO·카카오페이·네이버페이 등) 이체는 카테고리 other 사용.

★ n빵 감지용 입금 캡처:
  타인(개인)이 보내온 입금 이체도 추출하세요. 예) "배*현 → 내 카카오페이", "김*준 → 내 계좌"
  이런 항목은 유형: 입금 으로 표시.
  은행 자동이체, 월급, 이자, 환급은 유형: 입금으로도 추출하지 마세요.

★★ 방향(화살표) 절대 규칙 — 가장 흔한 오류이니 반드시 지킬 것:
  화살표 "→" 의 오른쪽이 "내 ○○"(내 카카오페이/내 계좌/내 토스 등)이면 = 돈이 나에게 "들어온" 입금입니다.
    예) "홍*정 → 내 카카오페이"  → 유형: 입금  (절대 지출 아님!)
    예) "김*준 → 내 계좌"        → 유형: 입금
    ※ 토스 화면에서 파란색 금액 = 입금. 파란색이면 무조건 유형: 입금.
  반대로 "내 ○○ → 상대"(내가 왼쪽)이면 = 내가 보낸 것(지출/충전).
  ★ 사용처에는 화살표 방향을 그대로 보존하세요. "홍*정"만 쓰지 말고 "홍*정 → 내 카카오페이" 로.

출력 형식 (항목마다 반복):
날짜: YYYY-MM-DD
금액: 숫자만 (항상 양수, 절대값으로 출력)
사용처: 상호명
유형: 지출
카테고리: other
---

예시:
날짜: ${year}-05-20
금액: 70000
사용처: PAYCO결제
카테고리: other
---`
}
